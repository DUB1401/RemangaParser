import zipfile

def ProxiesExtension(UserName: str, Password: str, IP: str, Port: str) -> str:
    # Манифест дополнения.
    Manifest = """
    {
        "version": "1.0.0",
        "manifest_version": 2,
        "name": "Proxies",
        "permissions": [
            "proxy",
            "tabs",
            "unlimitedStorage",
            "storage",
            "<all_urls>",
            "webRequest",
            "webRequestBlocking"
        ],
        "background": {
            "scripts": ["background.js"]
        },
        "minimum_chrome_version":"22.0.0"
    }
    """
    # Выполняемый в фоне скрипт дополнения.
    BackgroundJS = """
    var config = {
            mode: "fixed_servers",
            rules: {
              singleProxy: {
                scheme: "http",
                host: "%s",
                port: parseInt(%s)
              },
              bypassList: ["localhost"]
            }
          };

    chrome.proxy.settings.set({value: config, scope: "regular"}, function() {});

    function callbackFn(details) {
        return {
            authCredentials: {
                username: "%s",
                password: "%s"
            }
        };
    }

    chrome.webRequest.onAuthRequired.addListener(
                callbackFn,
                {urls: ["<all_urls>"]},
                ['blocking']
    );
    """ % (IP, Port, UserName, Password)
    # Название файла дополнения.
    ExtensionFileName = "ProxyExtension.zip"

    # Создание *.zip файла дополнения.
    with zipfile.ZipFile(ExtensionFileName, "w") as ZipFile:
        ZipFile.writestr("manifest.json", Manifest)
        ZipFile.writestr("background.js", BackgroundJS)

    return ExtensionFileName
